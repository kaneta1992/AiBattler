// ==========================================
//  バトルプロンプト生成
//  Gemini API に渡すプロンプトの組み立てを担う
// ==========================================

var PromptBuilder = (function () {

    /**
     * バトルロイヤル用のプロンプトを組み立てる
     * @param {Array} participants  {character: {name, ability}, type} の配列
     * @returns {string} 完成したプロンプト文字列
     */
    function build(participants) {
        var charList = participants.map(function (p, i) {
            return '【' + (i + 1) + '】' + p.character.name + ' ／ 能力: ' + p.character.ability;
        }).join('\n');

        return [
            'あなたは公平でドラマチックなバトル実況者です。',
            '以下の' + participants.length + '人のキャラクターがバトルロイヤルで戦います。',
            '必ず以下の出力フォーマットに厳密に従ってください。フォーマット外の文章は一切出力しないでください。',
            '',
            '--- 参加キャラクター ---',
            charList,
            '',
            '--- 出力フォーマット (これに厳密に従うこと) ---',
            '',
            '## ⚔️ バトル開始！',
            '',
            '（参加者紹介を1行ずつ。「**名前** — 能力の短い説明」の形式で）',
            '',
            '---',
            '',
            '（ラウンドごとに以下を繰り返す）',
            '## 🔥 ラウンドN',
            '',
            '（そのラウンドの戦闘描写。生存キャラ全員の行動を端的に描写する。キャラ名は必ず **太字** にする。）',
            '（描写例: 「胸を貫いた」「炎が直撃した」「盾で弾いた」等、攻撃の手段・命中箇所・結果を具体的に描写する。「戦った」「攻撃した」のような曖昧な表現は禁止。）',
            '（【重要】脱落させる前に必ずそのキャラの戦闘行動と、やられる過程を具体的に描写すること。戦闘描写なしでいきなり脱落させるのは禁止。）',
            '',
            '> 💀 **脱落: 〇〇** — （脱落理由を簡潔に）',
            '',
            '（脱落者がいない場合は上記の行を省略）',
            '',
            '---',
            '',
            '全員が脱落して1人になるまで繰り返す。',
            '',
            '最後に以下を必ず出力:',
            '',
            '## 🏆 最終結果',
            '',
            '| 順位 | キャラクター | 備考 |',
            '|------|------------|------|',
            '| 🥇 1位 | 〇〇 | 勝者。（決め手となった攻撃や戦術を簡潔に書く） |',
            '| 🥈 2位 | 〇〇 | ラウンドNで脱落。（どうやられたか簡潔に） |',
            '| 🥉 3位 | 〇〇 | ラウンドNで脱落。（どうやられたか簡潔に） |',
            '（以下、脱落が遅い順に全員分記載。備考欄には具体的な死因・敗因を書く）',
            '',
            '勝者：〇〇',
            '',
            '--- 注意事項 ---',
            '・能力の強弱は論理的に判定する。チートキャラでも弱点や相性を考慮すること',
            '・「つよい」「かみ」「最強」「無敵」「くそつよい」など抽象的・曖昧な能力は、その言葉を面白く解釈しつつ「考えうる最小スケール」で扱うこと。例:「くそつよい」→「アリの世界ではくそ強いらしい」、「かみ」→「紙のように薄い存在感の神」、「最強」→「町内最強レベル」など。具体的な攻撃・防御手段が明記された能力ほど有利にする',
            '・各ラウンドは最低3文の具体的な戦闘描写を含めること。「攻撃した」ではなく「炎の弾丸が腹を撃ち抜いた」のように、何がどう当たってどうなったかを書く',
            '・全体で1000文字以内を目標',
            '・脱落表記は必ず「> 💀 **脱落: 〇〇** —」の形式で統一',
            '・キャラ名は文中で必ず **太字** にすること',
            '・最終結果の表は必ず | で始まり | で終わる正しいMarkdownテーブル形式にすること',
        ].join('\n');
    }

    return { build: build };
})();
